#!/usr/bin/env node
/**
 * Scans docs/ for MD/MDX files that use DynamicMarkdownSelector with options whose paths
 * contain /content/ or /supported-formats/. Builds a map: contentPath -> { parentRepoPath, hash }
 * and writes it to client-modules/generated/dmsRedirectMap.js for use by dmsContentRedirect.js.
 *
 * contentPath = path from option, normalized (no leading /, no .md).
 * parentRepoPath = doc path relative to docs/ (file containing the DMS), no extension.
 * hash = option label normalized (lowercase, no spaces) for URL fragment.
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.join(__dirname, '..');
const docsDir = path.join(repoRoot, 'docs');
const outDir = path.join(repoRoot, 'client-modules', 'generated');
const outFile = path.join(outDir, 'dmsRedirectMap.js');

/** Normalize label to hash: lowercase, no spaces (match DMS). */
function labelToHash(label) {
  return (label || '').toLowerCase().replace(/\s+/g, '');
}

/** Normalize option path to content path key (no leading /, no .md). */
function pathToContentKey(optionPath) {
  return optionPath
    .replace(/^\//, '')
    .replace(/\.(md|mdx)$/i, '')
    .trim();
}

/** Collapse consecutive duplicate path segments so URL matches site (e.g. get-started/get-started -> get-started). */
function collapseDuplicateSegments(repoPath) {
  const segments = repoPath.split('/').filter(Boolean);
  const out = [];
  for (const seg of segments) {
    if (out[out.length - 1] !== seg) out.push(seg);
  }
  return out.join('/');
}

/** Extract option key from the line that precedes path: "..." (e.g. "    AWS: {" or "    \"Hugging Face\": {"). */
function findKeyBeforePath(text, pathMatchIndex) {
  const before = text.slice(0, pathMatchIndex);
  // Last occurrence of key: { (key can be "Quoted", 'Quoted', or Word)
  const keyRegex = /(?:^|\n)\s*("([^"]*)"|'([^']*)'|([A-Za-z0-9]+(?:\s+[A-Za-z0-9]+)*))\s*:\s*\{/g;
  let lastKey = null;
  let m;
  while ((m = keyRegex.exec(before)) !== null) {
    lastKey = (m[2] ?? m[3] ?? m[4] ?? '').trim();
  }
  return lastKey || null;
}

function* walkDocs(dir, relativePrefix = '') {
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  for (const e of entries) {
    const rel = relativePrefix ? `${relativePrefix}/${e.name}` : e.name;
    if (e.isDirectory()) {
      yield* walkDocs(path.join(dir, e.name), rel);
    } else if (e.isFile() && /\.(md|mdx)$/i.test(e.name)) {
      yield { filePath: path.join(dir, e.name), relativePath: rel };
    }
  }
}

/** Parse a single file for DMS options with content/supported-formats paths. */
function extractOptions(fileContent, parentRepoPath) {
  parentRepoPath = parentRepoPath.replace(/\.(md|mdx)$/i, '');
  const entries = [];
  // Match path: "/...content..." or path: '/...content...' or path: "...supported-formats..."
  const pathRegex = /path:\s*["']([^"']*(?:content|supported-formats)[^"']*)["']/g;
  let m;
  while ((m = pathRegex.exec(fileContent)) !== null) {
    const optionPath = m[1].trim();
    const key = findKeyBeforePath(fileContent, m.index);
    const contentKey = pathToContentKey(optionPath);
    const hash = key
      ? encodeURIComponent(labelToHash(key))
      : encodeURIComponent(contentKey.split('/').pop() || '');
    entries.push({ contentKey, parentRepoPath, hash });
  }
  return entries;
}

function main() {
  const map = Object.create(null);

  for (const { filePath, relativePath } of walkDocs(docsDir)) {
    const raw = fs.readFileSync(filePath, 'utf-8');
    if (!raw.includes('DynamicMarkdownSelector') || (!raw.includes('/content/') && !raw.includes('/supported-formats/'))) {
      continue;
    }

    const entries = extractOptions(raw, relativePath);
    for (const { contentKey, parentRepoPath, hash } of entries) {
      // Prefer first parent (first doc that lists this content path)
      if (!map[contentKey]) {
        map[contentKey] = {
          parentRepoPath: collapseDuplicateSegments(parentRepoPath),
          hash,
        };
      }
    }
  }

  fs.mkdirSync(outDir, { recursive: true});
  const content = `/**
 * Generated by scripts/generate-dms-redirect-map.mjs. Do not edit.
 * Maps content path (no /docs/, no .md) -> { parentRepoPath, hash } for DMS redirects.
 * CommonJS so bundlers (including Netlify) always include it.
 */
// eslint-disable-next-line no-unused-vars
const DMS_REDIRECT_MAP = ${JSON.stringify(map, null, 2)};
module.exports = DMS_REDIRECT_MAP;
`;
  fs.writeFileSync(outFile, content, 'utf-8');
  const count = Object.keys(map).length;
  console.log(`Wrote ${count} DMS redirect entries to ${path.relative(repoRoot, outFile)}`);
}

main();
